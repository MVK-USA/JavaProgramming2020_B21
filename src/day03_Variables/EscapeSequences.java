package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello Everyone");
        System.out.println("My name is Cybertek, I am from Virginia");

        System.out.println("========================================");

        System.out.println("Hello Everyone\nMy name is Cybertek, I am from Virginia");


        // \t - tab key
        System.out.println("\tToday I learn JAVA");
        System.out.println("it was so much fun");

        System.out.println("===================================================");

        //  \\: \
        System.out.println("/");
        System.out.println("\\"); // prints single backslash

        System.out.println("===================================================");

        System.out.println("My name is 'Mariia'");
        System.out.println("My name is \'Mariia\'");

        System.out.println("========================================================");

        System.out.println("My favourite show is \"Game\" ");


    }

}
